DROP PROCEDURE IF EXISTS P_READ_ALL_DDO
GO

CREATE PROCEDURE [dbo].[P_READ_ALL_DDO]

AS

BEGIN TRY
	
	SELECT
		DDO_ID,
		DDO_CODE,
		DEPT_ID
	FROM 
		DDO_MASTER
END TRY 


BEGIN CATCH
	DECLARE @ERR  VARCHAR(1000)
	SELECT @ERR=ERROR_MESSAGE()
	IF	ERROR_SEVERITY () > 12
		BEGIN
			RAISERROR (@ERR, 16, 20)
		END
		--
		ELSE
		--
		BEGIN
			RAISERROR (@ERR, 12, 2)
		END
END CATCH

GO