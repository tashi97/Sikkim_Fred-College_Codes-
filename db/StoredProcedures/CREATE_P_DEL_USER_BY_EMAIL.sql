CREATE PROCEDURE [dbo].[P_DEL_USER_BY_EMAIL]
	@EMAIL BIGINT
AS

BEGIN TRY
	
	DELETE FROM TBL_USER WHERE [USER_NAME] = @EMAIL
	
END TRY 

BEGIN CATCH
	DECLARE @ERR  VARCHAR(1000)
	SELECT @ERR=ERROR_MESSAGE()
	IF	ERROR_SEVERITY () > 12
		BEGIN
			RAISERROR (@ERR, 16, 20)
		END
		--
		ELSE
		--
		BEGIN
			RAISERROR (@ERR, 12, 2)
		END
END CATCH

GO